export const load_label = (iconType, labelIndex, appLang) => {
  if (labelIndex === 0) {
    switch (iconType) {
      case "spotify":
        return appLang.editPage.modal.enter.musicPlaceHolder;
      case "soundcloud":
        return appLang.editPage.modal.enter.musicPlaceHolder;
      case "url":
        return appLang.editPage.modal.enter.namePlaceHolder;
      default:
        return appLang.editPage.modal.enter.namePlaceHolder;
    }
  }
  if (labelIndex === 1) {
    return appLang.editPage.modal.enter.artistPlaceHolder;
  }
  switch (iconType) {
    case "mail":
      return appLang.editPage.modal.enter.email;
    case "phoneNumber":
      return appLang.editPage.modal.enter.phone;
    case "bank":
      return appLang.editPage.modal.enter.bankInfo;
    default:
      return appLang.editPage.modal.enter.link;
  }
};

